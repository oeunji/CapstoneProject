import UIKit
import SnapKit
import FirebaseFirestore

final class SignUpProtectedViewController: UIViewController {
    private var selectedGender: String = "" // "M" or "F"
    
    // MARK: - UI ÏöîÏÜå
    private lazy var nameTextField = createTextField(placeholder: "Ïù¥Î¶Ñ")
    private lazy var usernameTextField = createTextField(placeholder: "ÏïÑÏù¥Îîî")
    private lazy var passwordTextField = createTextField(placeholder: "ÎπÑÎ∞ÄÎ≤àÌò∏", isSecure: true)
    private lazy var phoneNumberTextField = createTextField(placeholder: "Ìú¥ÎåÄÌè∞ Î≤àÌò∏")
    private lazy var birthTextField = createTextField(placeholder: "ÏÉùÎÖÑÏõîÏùº (YYYY-MM-DD)")
    private lazy var homeAddressTextField = createTextField(placeholder: "Ïßë Ï£ºÏÜå")
    private lazy var guardianPhoneNumberTextField = createTextField(placeholder: "Î≥¥Ìò∏Ïûê Ìú¥ÎåÄÌè∞ Î≤àÌò∏")

    private lazy var maleButton: UIButton = createGenderButton(title: "ÎÇ®ÏÑ±", gender: "M")
    private lazy var femaleButton: UIButton = createGenderButton(title: "Ïó¨ÏÑ±", gender: "F")
    
    private lazy var signUpButton: UIButton = {
        let button = UIButton(type: .custom)
        button.setTitle("ÌöåÏõêÍ∞ÄÏûÖ ÏôÑÎ£å", for: .normal)
        button.setTitleColor(.black, for: .normal)
        button.titleLabel?.font = UIFont(name: "Pretendard-Medium", size: 21)
        button.backgroundColor = UIColor.appColor(.mainTheme)
        button.layer.cornerRadius = 5
        button.addTarget(self, action: #selector(didTapSignUpButton), for: .touchUpInside)
        return button
    }()

    override func viewDidLoad() {
        super.viewDidLoad()
        setUI()
        setNavigationBar()
    }

    // MARK: - ÌöåÏõêÍ∞ÄÏûÖ Î≤ÑÌäº ÌÅ¥Î¶≠
    @objc private func didTapSignUpButton() {
        guard let username = usernameTextField.text, !username.isEmpty,
              let name = nameTextField.text, !name.isEmpty,
              let password = passwordTextField.text, !password.isEmpty,
              let phone = phoneNumberTextField.text, !phone.isEmpty,
              let birthdate = birthTextField.text, !birthdate.isEmpty,
              let homeAddress = homeAddressTextField.text, !homeAddress.isEmpty,
              let guardianPhoneNumber = guardianPhoneNumberTextField.text, !guardianPhoneNumber.isEmpty
        else {
            print("Î™®Îì† ÌïÑÎìúÎ•º ÏûÖÎ†•Ìï¥Ï£ºÏÑ∏Ïöî.")
            return
        }

        let db = Firestore.firestore()
        let userData: [String: Any] = [
            "username": username,
            "name": name,
            "password": password,
            "phone": phone,
            "birthdate": birthdate,
            "gender": selectedGender,
            "home_address": homeAddress,
            "guardian_phone": guardianPhoneNumber
        ]

        db.collection("protected_users").document(username).setData(userData) { error in
            if let error = error {
                print("üö® Firestore Ï†ÄÏû• Ïã§Ìå®: \(error.localizedDescription)")
            } else {
                self.navigationController?.popToRootViewController(animated: true)
            }
        }
    }

    // MARK: - UI ÏÑ§Ï†ï
    private func setUI() {
        view.backgroundColor = .white
        let stackView = UIStackView(arrangedSubviews: [
            nameTextField, usernameTextField, passwordTextField, phoneNumberTextField, birthTextField, homeAddressTextField, guardianPhoneNumberTextField, maleButton, femaleButton
        ])
        
        stackView.axis = .vertical
        stackView.spacing = 15
        stackView.alignment = .fill
        stackView.distribution = .fillEqually

        view.addSubview(stackView)
        view.addSubview(signUpButton)
        
        stackView.snp.makeConstraints { make in
            make.centerX.equalTo(view)
            make.centerY.equalTo(view)
            make.leading.equalTo(view.snp.leading).offset(30)
            make.trailing.equalTo(view.snp.trailing).offset(-30)
            make.height.equalTo(500)
        }
        
        signUpButton.snp.makeConstraints { make in
            make.leading.equalTo(view.snp.leading).offset(30)
            make.trailing.equalTo(view.snp.trailing).offset(-30)
            make.top.equalTo(stackView.snp.bottom).offset(15)
            make.height.equalTo(50)
        }
    }

    // MARK: - ÌÖçÏä§Ìä∏ ÌïÑÎìú ÏÉùÏÑ± Ìï®Ïàò
    private func createTextField(placeholder: String, isSecure: Bool = false) -> UITextField {
        let textField = UITextField()
        textField.placeholder = placeholder
        textField.isSecureTextEntry = isSecure
        textField.textColor = .black  // üîπ ÌÖçÏä§Ìä∏ ÏÉâÏÉÅ ÏôÑÏ†Ñ Í≤ÄÏ†ïÏÉâ
        textField.tintColor = .black  // üîπ Ïª§ÏÑú ÏÉâÏÉÅÎèÑ Í≤ÄÏ†ïÏÉâ
        textField.font = UIFont(name: "Pretendard-Regular", size: 18) // üîπ Ìè∞Ìä∏ Î≥ÄÍ≤Ω

        // üîπ Ïª§Ïä§ÌÖÄ ÌÖåÎëêÎ¶¨ Ïä§ÌÉÄÏùº Ï†ÅÏö©
        textField.layer.borderWidth = 1.0  // ÌÖåÎëêÎ¶¨ ÎëêÍªò ÏÑ§Ï†ï
        textField.layer.borderColor = UIColor.lightGray.cgColor
        textField.layer.cornerRadius = 5  // Îë•Í∑º Î™®ÏÑúÎ¶¨ Ï†ÅÏö©
        textField.backgroundColor = .white // Î∞∞Í≤ΩÏÉâ Ìù∞ÏÉâ Ïú†ÏßÄ

        textField.autocapitalizationType = .none
        textField.autocorrectionType = .no
        textField.spellCheckingType = .no
        textField.leftView = UIView(frame: CGRect(x: 0, y: 0, width: 10, height: 0))
        textField.leftViewMode = .always
        
        return textField
    }

    
    // MARK: - ÏÑ±Î≥Ñ ÏÑ†ÌÉù Î≤ÑÌäº ÏÉùÏÑ±
    private func createGenderButton(title: String, gender: String) -> UIButton {
        let button = UIButton(type: .custom)
        button.setTitle(title, for: .normal)
        button.setTitleColor(.black, for: .normal)
        button.backgroundColor = .white
        button.layer.borderColor = UIColor.lightGray.cgColor
        button.layer.borderWidth = 1.0
        button.layer.cornerRadius = 5
        button.titleLabel?.font = UIFont(name: "Pretendard-Regular", size: 18)
        button.addTarget(self, action: #selector(didTapGenderButton(_:)), for: .touchUpInside)
        button.tag = gender == "M" ? 1 : 2
        return button
    }
    
    @objc private func didTapGenderButton(_ sender: UIButton) {
        selectedGender = sender.tag == 1 ? "M" : "F"
        maleButton.backgroundColor = sender.tag == 1 ? .darkGray : .white
        femaleButton.backgroundColor = sender.tag == 2 ? .darkGray : .white
    }
}

extension SignUpProtectedViewController {
    private func setNavigationBar() {
        let appearance = UINavigationBarAppearance()
        appearance.backgroundColor = UIColor.appColor(.mainTheme)
        
        appearance.titleTextAttributes = [
            .font: UIFont(name: "Pretendard-Medium", size: 18) ?? UIFont.systemFont(ofSize: 18),
            .foregroundColor: UIColor.white
        ]
        
        navigationController?.navigationBar.standardAppearance = appearance
        navigationController?.navigationBar.scrollEdgeAppearance = appearance
        navigationController?.navigationBar.tintColor = .white
        
        navigationItem.title = "ÌîºÎ≥¥Ìò∏Ïûê ÌöåÏõêÍ∞ÄÏûÖ"
        
        navigationItem.leftBarButtonItem = UIBarButtonItem(
            image: UIImage(systemName: "chevron.left"),
            style: .plain,
            target: self,
            action: #selector(didTapLeftBarButton)
        )
    }
    
    @objc private func didTapLeftBarButton() {
        navigationController?.popViewController(animated: true)
    }
}
